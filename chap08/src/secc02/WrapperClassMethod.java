package secc02;

public class WrapperClassMethod {

	public static void main(String[] args) {
		Integer n1 = 10;
		Integer n2 = 1024;

		System.out.println("큰 수 : " + Integer.max(n1, n2));
		System.out.println("작은 수 : " + Integer.min(n1, n2));
		System.out.println("합 : " + Integer.sum(n1, n2));
		System.out.println();

	}

}
